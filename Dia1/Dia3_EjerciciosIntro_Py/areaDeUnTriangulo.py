# Entrada: Queremos conocer la base y altura del triángulo
print('Ingresa la altura del triángulo')
Altura = float(input())
print('Ingresa la base del triángulo')
Base = float(input())

# Proceso: Para conocer el área del triángulo utilizamos la siguiente fórmula: (1/2*base*Altura)
Area = 1/2 * Base * Altura

# Salida: Queremos mostrar nuestro resultado (Area), imprimimos Area.
print(Area)