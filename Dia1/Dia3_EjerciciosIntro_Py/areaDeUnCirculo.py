# Entrada: Queremos conocer el radio del círculo
print('Ingresa el radio de tu círculo')
radio = float(input())

# Proceso: Para poder conocer el área del círculo, lo haremos con la siguiente fórmula: Área = pi * radio**2
Area = 3.1416 * radio ** 2

# Salida: Queremos mostrar el valor del Área del círculo.
print('Tu círculo tiene un área de: ', Area)
