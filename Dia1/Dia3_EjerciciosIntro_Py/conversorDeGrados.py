# Queremos conocer los grados Celsius
print('Ingresa los grados Celsius que deseas convertir: ')
Celsius = float(input())

# Proceso: Multiplicamos grados Celsius por 9/5 y le sumamos 32. El resultado es la conversión de Celsius a Fahrenheit
Fahrenheit = Celsius * 9/5 + 32

# Salida: Queremos mostrar la conversión de grados, para esto imprimimos 'Fahrenheit'
print(Fahrenheit)
